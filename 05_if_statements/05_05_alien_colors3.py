# if-elif-else chain.

# Running the if block.
alien_color = 'green'

if alien_color == 'green':
    print('5 points rewarded')
elif alien_color == 'yellow':
    print('10 points rewarded')
else:
    print('15 points rewarded')

# Running the elif block.
alien_color = 'yellow'

if alien_color == 'green':
    print('5 points rewarded')
elif alien_color == 'yellow':
    print('10 points rewarded')
else:
    print('15 points rewarded')

# Running the else block.
alien_color = 'red'

if alien_color == 'green':
    print('5 points rewarded')
elif alien_color == 'yellow':
    print('10 points rewarded')
else:
    print('15 points rewarded')