# Printing in Dictionaries.

user_info = {
    'first_name': 'Kenny', 
    'last_name': 'Kerby', 
    'age': 22, 
    'city': 'Pottsville',
    }

# Printing first_name.
print(user_info['first_name'])

# Printing last_name.
print(user_info['last_name'])

# Printing age.
print(user_info['age'])

# Printing city.
print(user_info['city'])